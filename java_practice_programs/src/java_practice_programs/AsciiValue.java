package java_practice_programs;

public class AsciiValue {
	public static void main(String[] args) {
		char c1 = 'a';
		char c2 = 'b';

		int ascii = c1;
		int ascii1 = c2;

		System.out.println("Ascii value of" + " " + c1 + " " + "is" + " " + ascii);
		System.out.println("Ascii value of" + " " + c2 + " " + "is" + " " + ascii1);
	}
}
